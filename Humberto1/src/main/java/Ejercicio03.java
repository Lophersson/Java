/**
 * Ejercicio 3: Suma de dos variables enteras
 * Declara dos variables enteras, asígnales valores, 
 * y luego muestra en la consola la suma de ambos valores
 */
public class Ejercicio03 {
    public static void main(String[] args) {
        // Declaración y asignación de variables enteras
        int numero1 = 15;
        int numero2 = 27;
        
        // Cálculo de la suma
        int suma = numero1 + numero2;
        
        // Mostrar resultados
        System.out.println("=== Suma de dos números enteros ===");
        System.out.println("Primer número: " + numero1);
        System.out.println("Segundo número: " + numero2);
        System.out.println("La suma es: " + numero1 + " + " + numero2 + " = " + suma);
    }
}